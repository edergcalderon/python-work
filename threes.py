# Create an empty list to store the multiples of 3
multiples_of_three = []

# Loop through the numbers from 3 to 30 (inclusive), with a step of 3
for number in range(3, 31, 3):
    # Append each number to the list
    multiples_of_three.append(number)

# Print the list of multiples of 3
print(multiples_of_three)